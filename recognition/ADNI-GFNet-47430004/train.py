def train_one_epoch(model: torch.nn.Module, criterion,
                    data_loader: Iterable, optimizer: torch.optim.Optimizer,
                    device: torch.device, epoch: int, loss_scaler, max_norm: float = 0,
                    model_ema = None, mixup_fn: Optional[Mixup] = None,
                    set_training_mode=True):
    model.train(set_training_mode)
    metric_logger = utils.MetricLogger(delimiter="  ")
    metric_logger.add_meter('lr', utils.SmoothedValue(window_size=1, fmt='{value:.6f}'))
    header = 'Epoch: [{}]'.format(epoch)
    print_freq = 400

    for samples, targets in metric_logger.log_every(data_loader, print_freq, header):
        samples = samples.to(device, non_blocking=True)
        targets = targets.to(device, non_blocking=True)

        if mixup_fn is not None:
            samples, targets = mixup_fn(samples, targets)

        outputs = model(samples)
        loss = criterion(outputs, targets)

        loss_value = loss.item()

        if not math.isfinite(loss_value):
            print("Loss is {}, stopping training".format(loss_value))
            sys.exit(1)

        optimizer.zero_grad()

        # this attribute is added by timm on one optimizer (adahessian) - use Adam
        # is_second_order = hasattr(optimizer, 'is_second_order') and optimizer.is_second_order
        # loss_scaler(loss, optimizer, clip_grad=max_norm,
        #                 parameters=model.parameters(), create_graph=is_second_order)

        loss.backward()
        optimizer.step()

        if device == 'cuda':
            torch.cuda.synchronize()

        metric_logger.update(loss=loss_value)
        metric_logger.update(lr=optimizer.param_groups[0]["lr"])
    # gather the stats from all processes
    metric_logger.synchronize_between_processes()
    print("Averaged stats:", metric_logger)
    return {k: meter.global_avg for k, meter in metric_logger.meters.items()}


@torch.no_grad()
def evaluate(data_loader, model, device):
    criterion = torch.nn.CrossEntropyLoss()

    metric_logger = utils.MetricLogger(delimiter="  ")
    header = 'Test:'

    # switch to evaluation mode
    model.eval()

    for images, target in metric_logger.log_every(data_loader, 100, header):
        images = images.to(device, non_blocking=True)
        target = target.to(device, non_blocking=True)

        # compute output
        output = model(images)
        loss = criterion(output, target)

        acc1 = accuracy(output, target, topk=(1,))[0]

        batch_size = images.shape[0]
        metric_logger.update(loss=loss.item())
        metric_logger.meters['acc1'].update(acc1.item(), n=batch_size)
    # gather the stats from all processes
    metric_logger.synchronize_between_processes()
    print('* Acc@1 {top1.global_avg:.3f} loss {losses.global_avg:.3f}'
          .format(top1=metric_logger.acc1, losses=metric_logger.loss))

    return {k: meter.global_avg for k, meter in metric_logger.meters.items()}

if __name__ == '__main__':
    print("Training GFNet for ADNI brain data\n")
    parser = argparse.ArgumentParser('GFNet training and evaluation script', parents=[get_args_parser()])
    args = parser.parse_args()
    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    print(device)

    print(args)
    
    if not os.path.exists(args.output_dir):
        os.makedirs(args.output_dir)

    criterion = torch.nn.CrossEntropyLoss()
    train_loader, test_loader = get_dataloaders(None, batch_size=args.batch_size)
    epoch = 0

    model = GFNet(num_classes=2, in_chans=1, drop_rate=args.drop)
    model.to(device)

    optimizer = create_optimizer(args, model)
    loss_scaler = NativeScaler()
    lr_scheduler, _ = create_scheduler(args, optimizer)

    print(f"Start training for {args.epochs} epochs")
    print(f"Hyperparameters:")
    print(f"lr = {args.lr}")
    start_time = time.time()
    max_accuracy = 0.0
    training_losses = []
    test_losses = []
    test_acc = []
    for epoch in range(args.start_epoch, args.epochs):
        
        train_stats = train_one_epoch(
            model, criterion, train_loader,
            optimizer, device, epoch, loss_scaler)

        lr_scheduler.step(epoch)
        
        if (epoch + 1) % 20 == 0:
            file_name = 'checkpoint_epoch%d.pth' % epoch
            checkpoint_path = os.path.join(args.output_dir, file_name)
            utils.save_on_master({
                'model': model.state_dict(),
                'optimizer': optimizer.state_dict(),
                'lr_scheduler': lr_scheduler.state_dict(),
                'epoch': epoch,
                'scaler': loss_scaler.state_dict(),
            }, checkpoint_path)

        test_stats = evaluate(test_loader, model, device)
        print(f"Accuracy of the network on the {len(test_loader)} test images: {test_stats['acc1']:.1f}%")
        training_losses.append(train_stats["loss"])
        test_losses.append(test_stats["loss"])
        test_acc.append(test_stats["acc1"])
        max_accuracy = max(max_accuracy, test_stats["acc1"])
        print(f'Max accuracy: {max_accuracy:.2f}%')

        if max_accuracy == test_stats["acc1"]:
            checkpoint_path = os.path.join(args.output_dir, 'checkpoint_best.pth')
            utils.save_on_master({
                    'model': model.state_dict(),
                    'optimizer': optimizer.state_dict(),
                    'lr_scheduler': lr_scheduler.state_dict(),
                    'epoch': epoch,
                    'scaler': loss_scaler.state_dict(),
                }, checkpoint_path)

        log_stats = {**{f'train_{k}': v for k, v in train_stats.items()},
                     **{f'test_{k}': v for k, v in test_stats.items()},
                     'epoch': epoch}

        if args.output_dir and utils.is_main_process():
            with open(args.output_dir + "log.txt", "a") as f:
                f.write(json.dumps(log_stats) + "\n")

    total_time = time.time() - start_time
    total_time_str = str(datetime.timedelta(seconds=int(total_time)))
    print('Training time {}'.format(total_time_str))

    if not os.path.exists(args.output_dir + "figs/"):
        os.makedirs(args.output_dir + "figs/")
    
    plt.figure(figsize=(10,5))
    plt.title("Training and Test Loss per epoch")
    plt.plot(training_losses, label="Training Loss")
    plt.plot(test_losses, label="Test Loss")
    plt.xlabel("Epoch")
    plt.ylabel("loss")
    plt.legend()
    plt.savefig(args.output_dir + "figs/Losses")
    plt.clf()
    plt.figure(figsize=(10,5))
    plt.title("Accuracy over time")
    plt.plot(test_acc, label="Acc @ 1")
    plt.xlabel("Epoch")
    plt.ylabel("Acc1")
    plt.legend()
    plt.savefig(args.output_dir + "figs/Accs")